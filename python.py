from fpdf import FPDF

class PDF(FPDF):
    def header(self):
        self.set_font("Arial", "B", 12)
        self.cell(0, 10, "مقارنة بين موقعي مفارش الحبيب والسعد هوم", ln=True, align="C")

    def chapter_title(self, title):
        self.set_font("Arial", "B", 12)
        self.set_text_color(0, 0, 128)
        self.cell(0, 10, title, ln=True, align="R")
        self.ln(2)

    def chapter_body(self, body):
        self.set_font("Arial", "", 12)
        self.set_text_color(0)
        self.multi_cell(0, 10, body, align="R")
        self.ln()

pdf = PDF()
pdf.add_page()
pdf.set_auto_page_break(auto=True, margin=15)

# إضافة المحتوى
sections = [
    ("1. عرض المنتجات", """
- مفارش الحبيب: يوفر تنوعاً واسعاً من المفارش، البطانيات، الوسائد، إكسسوارات المنزل، ومنتجات الأطفال، مع تصنيفات واضحة تسهّل التصفح.
- السعد هوم: يوفر منتجات أنيقة وجودة عالية، لكن التنوع أقل من مفارش الحبيب.

اقتراح تحسين للسعد:
زيادة تنوع الفئات، وخاصة فئة الأطفال والحمامات، مع إبراز المنتجات الجديدة والعروض.
"""),
    ("2. صور المنتجات", """
- مفارش الحبيب: صور بجودة عالية تُظهر التفاصيل والمنتج من زوايا متعددة.
- السعد هوم: صور احترافية لكن بعدد أقل وزوايا محدودة.

اقتراح تحسين للسعد:
إضافة صور من زوايا متعددة مع تقريب للملمس وتفاصيل المنتج.
"""),
    ("3. تجربة المستخدم وسهولة التصفح", """
- مفارش الحبيب: تصميم بسيط وواضح، أقسام محددة، تطبيق موبايل متاح.
- السعد هوم: تصميم حديث، لكن يحتاج تبسيط أكبر للقوائم.

اقتراح تحسين للسعد:
تحسين تنظيم الأقسام وإضافة فلاتر بحث أكثر وضوحاً للمنتجات.
"""),
    ("4. الشحن وخدمة العملاء", """
- مفارش الحبيب: شحن سريع، استرجاع مرن، دعم متعدد القنوات.
- السعد هوم: شحن جيد، دعم متوفر لكن يحتاج تحسين في شروط الاسترجاع.

اقتراح تحسين للسعد:
تبسيط سياسة الإرجاع وزيادة الوضوح في تفاصيل الشحن.
"""),
    ("5. الأسعار والعروض", """
- مفارش الحبيب: أسعار تنافسية، خصومات، وعروض موسمية.
- السعد هوم: جودة عالية بأسعار معقولة، لكن عروض أقل.

اقتراح تحسين للسعد:
زيادة الحملات الترويجية والخصومات الموسمية وتفعيل برامج ولاء.
""")
]

for title, body in sections:
    pdf.chapter_title(title)
    pdf.chapter_body(body)

pdf.output("مقارنة_مواقع_المفروشات.pdf")
